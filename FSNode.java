public class FSNode {
    private String name;
    protected int size;
    protected String path;

    public FSNode(String name, int size, String path) {
        this.name = name;
        this.size = size;
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }

    public String getPath() {
        return path;
    }
}
